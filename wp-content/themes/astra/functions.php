function custom_theme_assets() {
    // Enqueue main stylesheet
    wp_enqueue_style('custom-style', get_stylesheet_uri());

    // Enqueue Bootstrap CSS file
    wp_enqueue_style('bootstrap-css', get_template_directory_uri() . '/assets/css/bootstrap/bootstrap.min.css', array(), '1.0', 'all');

    // Enqueue Custom CSS file
    wp_enqueue_style('custon-theme-css', get_template_directory_uri() . '/assets/css/custom.css', array(), '1.0', 'all');    

    // Enqueue jQuery (WordPress includes it by default)
    wp_enqueue_script('jquery');
}

