function custom_footer_logo_shortcode() {
    // Get custom logo URL
    $custom_logo_id = get_theme_mod('custom_logo');
    $logo_url = wp_get_attachment_image_src($custom_logo_id, 'custom-size');

    // Check if logo exists
    if ($logo_url) {
        return '<a href="#" class="footer-site-logo"><img width="115" height="50" src="' . esc_url($logo_url[0]) . '" alt="' . get_bloginfo('name') . '" class="footer-logo"></a>';
    } else {
        // Fallback: Show site name if no logo is set
        return '<h2 class="footer-site-title">' . get_bloginfo('name') . '</h2>';
    }
}

// Register shortcode
add_shortcode('footer_logo', 'custom_footer_logo_shortcode');

