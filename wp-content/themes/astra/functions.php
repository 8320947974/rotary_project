function recent_posts_shortcode($atts) {
    // Extract attributes (default posts count: 5)
    $atts = shortcode_atts(array(
        'count' => 5, // Number of posts to show
    ), $atts, 'recent_posts');

    $query_args = array(
        'post_type'      => 'product', // Change this to a custom post type if needed
        'posts_per_page' => intval($atts['count']),
        'post_status'    => 'publish',
        'orderby'        => 'date',
        'order'          => 'DESC',
    );

    $query = new WP_Query($query_args);
    $output = '<ul>';

    if ($query->have_posts()) {
        while ($query->have_posts()) {
            $query->the_post();
            $output .= '<li><a href="' . get_permalink() . '">' . get_the_title() . '</a></li>';
        }
        wp_reset_postdata();
    } else {
        $output .= '<li>No recent posts found.</li>';
    }

    $output .= '</ul>';
    return $output;
}
add_shortcode('recent_posts', 'recent_posts_shortcode');

